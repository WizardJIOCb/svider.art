const repaired = works.map((work) => {
  const needsRepair = /\?{3,}/.test(`${work.shortDescription || ""} ${work.fullDescription || ""}`);
  if (!needsRepair) {
    return work;
  }

  const template = collectionTemplates[work.collectionId];
  const override = specificOverrides[work.id] || {};
  const shortDescription =
    override.short ||
    template?.short ||
    "Лист из коллекции Сергея Свидера, построенный на внимании к состоянию, линии и материальности отпечатка.";
  const fullDescription =
    override.full ||
    template?.full?.replaceAll("{title}", work.title) ||
    `${work.title} — работа Сергея Свидера, в которой важны камерный масштаб, характер линии и внимательное отношение к художественному состоянию листа.`;

  return {
    ...work,
    shortDescription,
    fullDescription,
  };
});

